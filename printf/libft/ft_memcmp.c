#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int						comp;
	unsigned const char		*str1;
	unsigned const char		*str2;
	unsigned int			i;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	while (i < n)
	{
		if (str1[i] == str2[i])
			i++;
		else
		{
			comp = (str1[i] - str2[i]);
			return (comp);
		}
	}
	return (0);
}
