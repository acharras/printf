#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char		*tab;
	size_t		i;
	size_t		len;

	i = 0;
	len = ft_strlen(s1);
	if (!(tab = malloc(sizeof(char) * (len + 1))))
		return (0);
	while (s1[i] != '\0')
	{
		tab[i] = s1[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
