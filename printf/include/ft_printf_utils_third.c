int		ft_int_len(int nbr)
{
	int		len;

	len = 1;
	if (nbr < 0)
		len++;
	while (nbr / 10 >= 1 || nbr / 10 <= -1)
	{
		nbr /= 10;
		len += 1;
	}
	return (len);
}

int		ft_u_int_len(unsigned int nbr)
{
	int		len;

	len = 1;
	while (nbr >= 10)
	{
		nbr /= 10;
		len += 1;
	}
	return (len);
}
